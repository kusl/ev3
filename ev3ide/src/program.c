//****************************************************************************
//Linux Includes
//--------------
#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <string.h>


//****************************************************************************
//lms includes
//------------
#include "lms2012.h"
#include "d_lcd.h"

//****************************************************************************
//Own includes
//-------------
#include "MyLib.h"


//****************************************************************************
// GLOBAL VARIABLES
//-----------------
LCD my_lcd;



//****************************************************************************
// MAIN PROGRAM
//-------------
int main()
{
	//****************************************************************************
	//INITIALISE LCD
	//---------------
	dLcdInit(my_lcd.Lcd);
	LCDClear(my_lcd.Lcd);

	//****************************************************************************
	//WRITE Hello World !
	//-------------------
	dLcdDrawText(my_lcd.Lcd, FG_COLOR, 1, 1, NORMAL_FONT, (signed char *)"Hello World!");
	dLcdUpdate(&my_lcd);

	//****************************************************************************
	//SLEEP 5 Seconds!
	//-------------------
	sleep(5);

	//****************************************************************************
	//CLEAR DISPLAY
	//-------------------
	LCDClear(my_lcd.Lcd);
	
	//****************************************************************************
	//CALL MyFunction in MyLib
	//------------------------
	MyFunction(); // this will print a text line in telnet window if you are connected by wifi!!

	//****************************************************************************
	//CLOSE LCD
	//---------------
	dLcdExit();
	
	//****************************************************************************
	//END MAIN
	//--------
	return 0;
}




